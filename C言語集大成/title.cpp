int g_TitleLoopCount;			// タイトルの描画を繰り返す為のカウンター


// タイトルの初期化処理
void InitTitle(void)
{
	g_TitleLoopCount = 0;

}


// タイトルの終了処理
void UninitTitle(void)
{

}


// タイトルの更新処理
void UpdateTitle(void)
{
	// テストでルパン３世を２回繰り返してみる
	g_TitleLoopCount++;
	if (g_TitleLoopCount > 2)
	{
		// キー入力待ち
		std::rewind(stdin);
		std::cin.get();

		SetMode(GAME_FIELD);		// Fieldに移動
	}

}


// タイトル画面処理
void DrawTitle(void)
{
	/* 画面描画処理 */
	for (int y = 0; y < DISP_H * ANIM_CNT; y++)
	{
		/* 1画面分表示したら少しウェイトさせる */
		if (y % DISP_H == 0)
		{
			/* ウェイト */
			std::this_thread::sleep_for(std::chrono::milliseconds(500));

			/* 画面クリア */
			system("cls");
		}

		// １文字ずつだと遅いから１行ずつ表示させる
		std::cout << &title[y][0];


	}

}



